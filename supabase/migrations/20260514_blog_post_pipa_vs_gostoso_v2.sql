<h2>4. Hospedagem e preços</h2>

<p>Em <strong>Pipa</strong>, alta temporada (dez–fev, julho) tem diária de pousada simples a partir de R$ 350. Pousadas charmosas pé na areia entre R$ 600–1.500. Aluguel de casa pra grupos: R$ 800–2.500/noite.</p>

<p>Em <strong>São Miguel do Gostoso</strong>, na mesma alta temporada, pousadas simples saem de R$ 200–350. Pousadas charme pé na areia: R$ 400–800. Casa pra grupo: R$ 500–1.500/noite. Em média, <strong>30–40% mais barato</strong> que Pipa.</p>

