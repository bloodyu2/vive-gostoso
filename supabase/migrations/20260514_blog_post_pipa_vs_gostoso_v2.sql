<h2>5. Quando ir</h2>

<h3>Melhor época pra Pipa</h3>
<p>Pipa é destino o ano inteiro, mas a <strong>baixa temporada (março–junho)</strong> é o segredo: dias de sol, mar morno, preços baixos, sem multidão. A alta (jul, dez–fev) tem o melhor agito mas o pior bolso e maior fila.</p>

<h3>Melhor época pra Gostoso</h3>
<p>Quem vai surfar/passear de buggy: qualquer época. Quem vai <strong>velejar</strong> (kite/wind): <strong>agosto a março</strong>, quando o vento vira leste e fica constante. Maio–julho tem vento mais fraco e dia menor — bom pra família e descanso, ruim pra esporte.</p>

<h2>6. Como chegar</h2>

<p>Os dois saem do mesmo aeroporto: <strong>Aeroporto de Natal (NAT)</strong>.</p>
<ul>
  <li><strong>Natal → Pipa:</strong> ~85 km, 1h45 de carro. Transfer compartilhado R$ 60–100/pessoa, particular R$ 250–400.</li>
  <li><strong>Natal → Gostoso:</strong> ~110 km, 2h de carro. Transfer particular R$ 300–450 (não há linha regular consistente; vale combinar antes).</li>
  <li><strong>Pipa → Gostoso:</strong> ~200 km, 3h30 de carro. Transfer particular R$ 600–900.</li>
</ul>

