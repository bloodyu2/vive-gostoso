</figure>

<p>Não tem falésias dramáticas tipo Pipa, mas tem algo que Pipa perdeu: <strong>silêncio</strong>. Mesmo no réveillon dá pra achar uma praia vazia 10 minutos depois do centro.</p>

<h2>2. O que fazer</h2>

